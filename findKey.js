const findKey = function(object, callback) {
  //loop through the object
  for (let key in object) {
    //call the callback function on object keys to return a truthy value if a match is found
    if (callback(object[key])) {
      return key;
    }
  }
  //return undefined if no match is found outside the for loop
  return undefined;
};

